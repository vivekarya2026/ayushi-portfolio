// ── Regenerate index.html home page featured list ──
async function buildHome(projects: Project[]) {
  let html: string;
  try {
    await backupOnce(INDEX_FILE);
    html = await readFile(INDEX_FILE, "utf8");
  } catch {
    console.warn("  ! index.html not found — skipping home page.");
    return;
  }
  const root = parse(html, { comment: true });
  const list = root.querySelector(".home-work-cms.w-dyn-items");
  if (!list) {
    console.warn(
      "  ! Could not find .home-work-cms.w-dyn-items in index.html — skipping home page.",
    );
    return;
  }

  // Featured projects first (in priority order); if none are featured, fall
  // back to the top projects by priority so the section is never empty.
  const featured = projects.filter((p) => p.featured);
  const chosen = (featured.length ? featured : projects).slice(
    0,
    HOME_FEATURED_LIMIT,
  );

  list.set_content(chosen.map(buildHomeItem).join(""));
  await writeFile(INDEX_FILE, applyResume(root.toString()), "utf8");
  console.log(`  ✓ index.html — ${chosen.length} featured item(s)`);
}

// ── Find a usable template project page (an original Webflow project file) ──
async function loadTemplateShell(): Promise<string> {
  const files = (await readdir(PROJECTS_DIR)).filter((f) =>
    f.endsWith(".html"),
  );
  // Prefer a known original page; fall back to the first file that is NOT
  // one we generated (so we always template from real Webflow markup).
  const preferred = ["xtudio.html", "schoolpal.html"];
  const candidates = [
    ...preferred.filter((f) => files.includes(f)),
    ...files.filter((f) => !preferred.includes(f)),
  ];
  for (const f of candidates) {
    const c = await readFile(join(PROJECTS_DIR, f), "utf8");
    if (!c.includes(GEN_MARKER)) return c;
  }
  // Everything is generated already — just reuse the first file as a shell.
  return readFile(join(PROJECTS_DIR, candidates[0]), "utf8");
}

// ── Regenerate a single project page from the template shell ──
function buildProjectPage(template: string, p: Project): string {
  const root = parse(template, { comment: true });

  // <html data-item-slug="..."> (de-Webflow renamed from data-wf-item-slug)
  const htmlEl = root.querySelector("html");
  if (htmlEl) htmlEl.setAttribute("data-item-slug", p.slug);

  // Title (hero h1)
  const h1 = root.querySelector(".work-hero_intro .heading-style-h1");
  if (h1) h1.set_content(esc(p.title));

  // Description paragraph
  const desc = root.querySelector(".work-body_descript p");
  if (desc) desc.set_content(esc(p.subtitle ?? ""));

  // Details: [0] category, [1] date
  const details = root.querySelectorAll(
    ".work-body_details .text-style-allcaps",
  );
  if (details[0]) details[0].set_content(esc(p.categories?.name ?? ""));
  if (details[1]) details[1].set_content(esc(formatDate(p.project_date)));

  // Live-project link
  const liveEl = root.querySelector(".work-body_details a.hyperlink");
  if (liveEl) {
    liveEl.setAttribute("href", p.live_link || "#");
    if (!p.live_link) liveEl.setAttribute("style", "display:none");
    else liveEl.removeAttribute("style");
  }

  // Rich text body
  const rich = root.querySelector(".rich-text-block.w-richtext, .w-richtext");
  if (rich) rich.set_content(renderBlocks(p.body?.content));

  // Mark as generated (right after <body>)
  let out = root.toString();
  if (!out.includes(GEN_MARKER)) {
    out = out.replace(/<body>/, `<body>${GEN_MARKER}`);
  }
  return applyResume(out);
}

// ── Rewrite resume links on standalone pages build:site doesn't regenerate ──
async function applyResumeToStandalonePages() {
  if (!RESUME_URL) return; // nothing to do
  for (const file of [ABOUT_FILE, CONTACT_FILE]) {
    let html: string;
    try {
      html = await readFile(file, "utf8");
    } catch {
      continue; // page may not exist
    }
    const swapped = applyResume(html);
    if (swapped === html) continue; // no legacy URLs present
    await backupOnce(file);
    await writeFile(file, swapped, "utf8");
    console.log(`  ✓ ${file.split("/").pop()} — resume link updated`);
  }
}

// Public site contact form → CMS /api/contact
function contactApiUrl(): string {
  const explicit = process.env.CONTACT_API_URL?.trim();
  if (explicit) return explicit.replace(/\/$/, "");
  const site = process.env.NEXT_PUBLIC_SITE_URL?.trim();
  if (site && !site.includes("localhost"))
    return `${site.replace(/\/$/, "")}/api/contact`;
  return "/api/contact";
}

async function applyContactApi() {
  const api = contactApiUrl();

  let html: string;
  try {
    html = await readFile(CONTACT_FILE, "utf8");
  } catch {
    return;
  }

  const next = html.replace(
    /<form([^>]*\bid=["']wf-form-Form["'][^>]*)>/i,
    (_match, attrs: string) => {
      if (/data-contact-api=/.test(attrs)) {
        return `<form${attrs.replace(
          /data-contact-api=["'][^"']*["']/,
          `data-contact-api="${api}"`,
        )}>`;
      }
      return `<form${attrs} data-contact-api="${api}">`;
    },
  );

  if (next === html) {
    if (/data-contact-api=["'][^"']*["']/.test(html)) {
      console.log(`  · contact.html — already → ${api}`);
    } else {
      console.log("  · contact form not found — skipped API wiring");
    }
    return;
  }
  await backupOnce(CONTACT_FILE);
  await writeFile(CONTACT_FILE, next, "utf8");
  console.log(`  ✓ contact.html — form → ${api}`);
}

async function main() {
  console.log("Fetching published projects from CMS…\n");

  // Resume URL is applied to every regenerated page (and about/contact).
  const { data: resumeRow } = await supabase
    .from("settings")
    .select("value")
    .eq("key", "resume_url")
    .maybeSingle();
  RESUME_URL = (resumeRow?.value as string | null) ?? null;
  if (RESUME_URL) console.log(`Resume link → ${RESUME_URL}\n`);

  const { data, error } = await supabase
    .from("projects")
    .select(
      "id, title, slug, subtitle, company_name, live_link, project_date, card_image_url, gallery, body, featured, sort_order, status, categories(name)",
    )
    .eq("status", "published")
    .order("sort_order", { ascending: true })
    .order("project_date", { ascending: false, nullsFirst: false });

  if (error) {
    console.error("Failed to load projects:", error.message);
    process.exit(1);
  }

  const projects = (data ?? []) as unknown as Project[];
  console.log(`Found ${projects.length} published project(s).\n`);

  // 1) Rebuild the works listing.
  await buildWorks(projects);

  // 1b) Rebuild the home page featured list.
  await buildHome(projects);

  // 2) Rebuild each project detail page.
  const template = await loadTemplateShell();
  const liveSlugs = new Set(projects.map((p) => p.slug));

  for (const p of projects) {
    const pageHtml = buildProjectPage(template, p);
    await writeFile(join(PROJECTS_DIR, `${p.slug}.html`), pageHtml, "utf8");
    console.log(`  ✓ projects/${p.slug}.html`);
  }

  // 2b) Rewrite resume links on standalone pages (about/contact).
  await applyResumeToStandalonePages();

  // 2c) Point the contact form at the CMS API.
  await applyContactApi();

  // 3) Remove generated project pages whose project no longer exists.
  const files = (await readdir(PROJECTS_DIR)).filter((f) =>
    f.endsWith(".html"),
  );
  for (const f of files) {
    const slug = f.replace(/\.html$/, "");
    if (liveSlugs.has(slug)) continue;
    const content = await readFile(join(PROJECTS_DIR, f), "utf8");
    if (content.includes(GEN_MARKER)) {
      await unlink(join(PROJECTS_DIR, f));
      console.log(`  – removed projects/${f} (unpublished/deleted)`);
    }
  }

  console.log("\nDone. The public site now reflects the CMS.");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
