// ── Regenerate works.html (replace the cards inside .work-list) ──
async function buildWorks(projects: Project[]) {
  await backupOnce(WORKS_FILE);
  const html = await readFile(WORKS_FILE, "utf8");
  const root = parse(html, { comment: true });
  const list = root.querySelector(".work-list.w-dyn-items");
  if (!list) {
    console.error("  ✗ Could not find .work-list.w-dyn-items in works.html");
    return;
  }
  const cardsHtml = projects.map(buildCard).join("");
  list.set_content(cardsHtml);
  await writeFile(WORKS_FILE, applyResume(root.toString()), "utf8");
  console.log(`  ✓ works.html — ${projects.length} card(s)`);
}

