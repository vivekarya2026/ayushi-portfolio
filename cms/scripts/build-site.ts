// ── One-time backup of an original Webflow file before we ever rewrite it ──
async function backupOnce(file: string) {
  const bak = `${file}.bak`;
  try {
    await access(bak);
    return; // backup already exists
  } catch {
    // no backup yet
  }
  try {
    const content = await readFile(file, "utf8");
    // Only back up genuine originals, not something we already generated.
    if (content.includes(GEN_MARKER)) return;
    await copyFile(file, bak);
    console.log(`  · backed up ${file.split("/").pop()} → *.bak`);
  } catch {
    // file may not exist yet; nothing to back up
  }
}

// ── Resume URL managed by the CMS (settings.resume_url); null = leave links ──
let RESUME_URL: string | null = null;

// Swap every legacy Webflow resume PDF link for the CMS-managed URL. If no
// resume is configured yet, this is a safe no-op (originals are left intact).
function applyResume(html: string): string {
  if (!RESUME_URL) return html;
  let out = html;
  for (const legacy of LEGACY_RESUME_URLS) {
    out = out.split(legacy).join(RESUME_URL);
  }
  return out;
}

// ── HTML escaping ──
function esc(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function escAttr(s: string): string {
  return esc(s);
}

function formatDate(iso: string | null): string {
  if (!iso) return "";
  // Stored as YYYY-MM-DD. Parse the parts directly to avoid UTC/local shifts.
  const m = /^(\d{4})-(\d{2})-(\d{2})/.exec(iso);
  const d = m
    ? new Date(Number(m[1]), Number(m[2]) - 1, Number(m[3]))
    : new Date(iso);
  if (Number.isNaN(d.getTime())) return "";
  return d.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  });
}

// ── Inline (marks) rendering: Tiptap inline nodes → HTML string ──
function renderInline(nodes: TiptapNode[] | undefined): string {
  if (!nodes) return "";
  return nodes
    .map((n) => {
      if (n.type === "hardBreak") return "<br/>";
      if (n.type !== "text") return renderInline(n.content);
      let html = esc(n.text ?? "");
      for (const mark of n.marks ?? []) {
        if (mark.type === "bold") html = `<strong>${html}</strong>`;
        else if (mark.type === "italic") html = `<em>${html}</em>`;
        else if (mark.type === "link") {
          const href = escAttr(String(mark.attrs?.href ?? "#"));
          html = `<a href="${href}">${html}</a>`;
        }
      }
      return html;
    })
    .join("");
}

// ── Block rendering: Tiptap doc → .w-richtext inner HTML ──
function renderBlocks(nodes: TiptapNode[] | undefined): string {
  if (!nodes) return "";
  const out: string[] = [];
  for (const node of nodes) {
    switch (node.type) {
      case "heading": {
        const level = Math.min(Math.max(Number(node.attrs?.level ?? 2), 1), 6);
        out.push(`<h${level}>${renderInline(node.content)}</h${level}>`);
        break;
      }
      case "paragraph": {
        const inner = renderInline(node.content);
        // Preserve Webflow's empty-paragraph spacer (zero-width joiner).
        out.push(`<p>${inner || "\u200d"}</p>`);
        break;
      }
      case "bulletList":
      case "orderedList": {
        const tag = node.type === "bulletList" ? "ul" : "ol";
        const items = (node.content ?? [])
          .map((li) => `<li>${renderInline(li.content?.[0]?.content)}</li>`)
          .join("");
        out.push(`<${tag} role="list">${items}</${tag}>`);
        break;
      }
      case "blockquote": {
        const inner = renderBlocks(node.content);
        out.push(`<blockquote>${inner}</blockquote>`);
        break;
      }
      case "horizontalRule": {
        out.push("<hr/>");
        break;
      }
      case "image": {
        const src = escAttr(String(node.attrs?.src ?? ""));
        const alt = escAttr(String(node.attrs?.alt ?? ""));
        const title = node.attrs?.title ? String(node.attrs.title) : "";
        const caption = title
          ? `<figcaption>${esc(title)}</figcaption>`
          : "";
        out.push(
          `<figure class="w-richtext-align-fullwidth w-richtext-figure-type-image"><div><img alt="${alt}" loading="lazy" src="${src}"/></div>${caption}</figure>`,
        );
        break;
      }
      default: {
        if (node.content) out.push(renderBlocks(node.content));
      }
    }
  }
  return out.join("");
}

