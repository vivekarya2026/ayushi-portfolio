/**
 * Static site generator: regenerate the original Webflow-designed public site
 * (works.html + projects/<slug>.html) from the CMS (Supabase), keeping the
 * exact Webflow markup, CSS and scripts intact.
 *
 * How it works:
 *   - works.html: the original file is used as-is; only the list of project
 *     cards inside `.work-list.w-dyn-items` is replaced with cards generated
 *     from the currently published CMS projects.
 *   - projects/<slug>.html: an original project page is used as a template
 *     "shell". Only the dynamic regions are swapped (title, description,
 *     category, date, live-link, and the .rich-text-block body). Everything
 *     else (navbar, footer, styles, scripts) stays byte-for-byte identical.
 *   - Project pages whose slugs no longer exist in the CMS (and that were
 *     previously generated by this script) are removed.
 *
 * Usage:
 *   npm run build:site
 *
 * Safe to re-run. Only published projects are rendered.
 */
